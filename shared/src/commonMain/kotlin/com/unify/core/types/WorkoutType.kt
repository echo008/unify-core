package com.unify.core.types

/**
 * 运动类型枚举
 * 定义了各种运动和健身活动类型
 */
enum class WorkoutType {
    /**
     * 跑步
     */
    RUNNING,

    /**
     * 步行
     */
    WALKING,

    /**
     * 骑行
     */
    CYCLING,

    /**
     * 游泳
     */
    SWIMMING,

    /**
     * 力量训练
     */
    STRENGTH_TRAINING,

    /**
     * 瑜伽
     */
    YOGA,

    /**
     * 有氧运动
     */
    CARDIO,

    /**
     * 其他运动
     */
    OTHER,
}

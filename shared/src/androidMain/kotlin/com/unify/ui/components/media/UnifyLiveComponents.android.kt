    @Composable
    fun AudioRecorder(
        modifier: Modifier = Modifier,
        onRecordingComplete: (String) -> Unit = {},
        onError: (String) -> Unit = {},
    ) {
        var isRecording by remember { mutableStateOf(false) }
        var recordingTime by remember { mutableStateOf(0) }
        var mediaRecorder by remember { mutableStateOf<android.media.MediaRecorder?>(null) }
        val context = LocalContext.current
        val coroutineScope = rememberCoroutineScope()

        LaunchedEffect(isRecording) {
            if (isRecording) {
                while (isRecording) {
                    kotlinx.coroutines.delay(1000)
                    recordingTime++
                }
            } else {
                recordingTime = 0
            }
        }

        Card(
            modifier = modifier.fillMaxWidth(),
            elevation = CardDefaults.cardElevation(defaultElevation = 4.dp),
        ) {
            Column(
                modifier = Modifier.padding(16.dp),
                horizontalAlignment = Alignment.CenterHorizontally,
            ) {
                Text(
                    text = if (isRecording) "ÂΩïÂà∂‰∏≠: ${formatTime(recordingTime * 1000)}" else "ÂáÜÂ§áÂΩïÂà∂",
                    style = MaterialTheme.typography.titleMedium,
                )

                Spacer(modifier = Modifier.height(16.dp))

                Button(
                    onClick = {
                        coroutineScope.launch {
                            if (isRecording) {
                                stopRecording(mediaRecorder, onRecordingComplete, onError)
                                isRecording = false
                                mediaRecorder = null
                            } else {
                                mediaRecorder = startRecording(context, onError)
                                if (mediaRecorder != null) {
                                    isRecording = true
                                }
                            }
                        }
                    },
                    colors =
                        ButtonDefaults.buttonColors(
                            containerColor =
                                if (isRecording) {
                                    MaterialTheme.colorScheme.error
                                } else {
                                    MaterialTheme.colorScheme.primary
                                },
                        ),
                ) {
                    Text(if (isRecording) "ÂÅúÊ≠¢ÂΩïÂà∂" else "ÂºÄÂßãÂΩïÂà∂")
                }
            }
        }
    }


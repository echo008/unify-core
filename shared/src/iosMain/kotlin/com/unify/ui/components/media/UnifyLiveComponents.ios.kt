    @Composable
    fun CameraPreview(
        modifier: Modifier = Modifier,
        onImageCaptured: (String) -> Unit = {},
        onError: (String) -> Unit = {}
    ) {
        var isRecording by remember { mutableStateOf(false) }
        var captureSession by remember { mutableStateOf<AVCaptureSession?>(null) }
        var photoOutput by remember { mutableStateOf<AVCapturePhotoOutput?>(null) }
        
        LaunchedEffect(Unit) {
            try {
                val session = AVCaptureSession()
                session.sessionPreset = AVCaptureSessionPresetHigh
                
                val device = AVCaptureDevice.defaultDeviceWithMediaType(AVMediaTypeVideo)
                if (device != null) {
                    val input = AVCaptureDeviceInput.deviceInputWithDevice(device, null)
                    if (input != null && session.canAddInput(input)) {
                        session.addInput(input)
                    }
                    
                    val output = AVCapturePhotoOutput()
                    if (session.canAddOutput(output)) {
                        session.addOutput(output)
                        photoOutput = output
                    }
                    
                    session.startRunning()
                    captureSession = session
                }
            } catch (e: Exception) {
                onError("Áõ∏Êú∫ÂàùÂßãÂåñÂ§±Ë¥•: ${e.message}")
            }
        }
        
        DisposableEffect(Unit) {
            onDispose {
                captureSession?.stopRunning()
            }
        }
        
        Column(modifier = modifier) {
            Box(
                modifier = Modifier
                    .fillMaxWidth()
                    .weight(1f)
                    .background(Color.Black),
                contentAlignment = Alignment.Center
            ) {
                Text(
                    text = "üì∑ iOSÁõ∏Êú∫È¢ÑËßà",
                    color = Color.White,
                    style = MaterialTheme.typography.headlineMedium
